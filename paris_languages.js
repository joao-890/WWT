function openLanguages() {
    var languages = document.querySelector('#most-spoken-languages');
    if (languages.style.display == "none") {
        languages.style.display = "flex";
    }
    else {
        languages.style.display = "none";
    }
}





function changeLanguage(language) {
    const selectedLanguage = bodyTexts[language];
    if (!selectedLanguage) {
        console.error(`Language '${language}' is not defined in bodyTexts.`);
        return;
    }
    document.getElementById("home_button").textContent = selectedLanguage.home;
    document.getElementById("marketing_button").textContent = selectedLanguage.expectations;
    document.getElementById("video_button").textContent = selectedLanguage.video;
    document.getElementById("comments_button").textContent = selectedLanguage.comment;
    document.getElementById("contacts_button").textContent = selectedLanguage.contact;
    document.querySelector(".marketing_text p").textContent = selectedLanguage.marketingText;
    // Update form fields
    document.getElementById("name").placeholder = selectedLanguage.form.namePlaceholder;
    document.getElementById("email").placeholder = selectedLanguage.form.emailPlaceholder;
    document.getElementById("message").placeholder = selectedLanguage.form.messagePlaceholder;
    document.querySelector(".submit-btn").value = selectedLanguage.form.submit;
    document.querySelector("#comment1 p").textContent = selectedLanguage.comments.comment1;
    document.querySelector("#comment2 p").textContent = selectedLanguage.comments.comment2;
    document.querySelector("#comment3 p").textContent = selectedLanguage.comments.comment3;


        
    
    openLanguages();
}
